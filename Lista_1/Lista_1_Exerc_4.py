lista_numeros = [5, 7, 2]
lista_strings = ["zé da manga", "mango joe", "mé da zanga"]

print(f"Lista não ordenada de números: {lista_numeros}")
print(f"Lista não ordenada de strings: {lista_strings}")

ordenada_lista_numeros = sorted(lista_numeros)
ordenada_lista_strings = sorted(lista_strings)

print(f"Lista ordenada de números: {ordenada_lista_numeros}")
print(f"Lista ordenada de strings: {ordenada_lista_strings}")
